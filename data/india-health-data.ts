// Generate random patient data with Indian names and conditions
export function generateIndianPatients(count: number) {
  const patients = []

  for (let i = 0; i < count; i++) {
    const isMale = Math.random() > 0.5
    const firstName = isMale
      ? indianNames.male[Math.floor(Math.random() * indianNames.male.length)]
      : indianNames.female[Math.floor(Math.random() * indianNames.female.length)]

    const lastName = indianNames.surnames[Math.floor(Math.random() * indianNames.surnames.length)]
    const age = Math.floor(Math.random() * 70) + 18
    const condition = indianHealthConditions[Math.floor(Math.random() * indianHealthConditions.length)]
    const hospital = indianHospitals[Math.floor(Math.random() * indianHospitals.length)]
    const state = indianStates[Math.floor(Math.random() * indianStates.length)]

    patients.push({
      id: i + 1,
      name: `${firstName} ${lastName}`,
      age,
      gender: isMale ? "Male" : "Female",
      condition,
      hospital,
      state,
      status: Math.random() > 0.7 ? "Critical" : Math.random() > 0.5 ? "Warning" : "Stable",
      lastUpdated: new Date(Date.now() - Math.floor(Math.random() * 7 * 24 * 60 * 60 * 1000)).toISOString(),
    })
  }

  return patients
}

